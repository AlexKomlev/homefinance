DROP TABLE ACCOUNTS;

CREATE TABLE ACCOUNTS(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
	NAME VARCHAR(64) NOT NULL,
	TYPE INTEGER NOT NULL,
	CREDIT_RATE INTEGER,
	LIMIT INTEGER,
  DESCRIPTION VARCHAR(64) NOT NULL,
	UNIQUE(NAME)
	);

